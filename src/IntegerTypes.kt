// In kotlin below are integer types:
// Byte (8-bits size)
// Short (16-bits size)
// Int (32-bits size)
// Long (64-bits size)

// And Float types are:
// Float (32-bits size)
// Double (64-bits size)

fun main() {
    val myByte: Byte = -126
    val myShort: Short = 125
    val myInt: Int = -123456789
    val myLong: Long = 39_812_309_487_120_300

    println(myByte)
    println(myShort)
    println(myInt)
    println(myLong)

    val myFloat: Float = 13.689F
    val myDouble: Double = 12456.49423476

    println(myFloat)
    println(myDouble)

}